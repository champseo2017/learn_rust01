// fn คือ keyword สำหรับประกาศ function
// main คือชื่อ function ที่เป็นจุดเริ่มต้นของโปรแกรม
// () บอกว่า function นี้ไม่มี parameters
fn main() {
    // โค้ดทั้งหมดจะอยู่ใน function body ที่ครอบด้วย {}

    // Style ของ Rust คือการ indent (ย่อหน้า) ด้วย 4 spaces
    // println! คือ macro สำหรับ print ข้อความออกทางหน้าจอ
    // "Hello, world!" คือ string ที่เป็น argument ส่งให้กับ macro
    // ; คือเครื่องหมายที่บอกว่าจบ expression หรือคำสั่งนี้แล้ว
    println!("Hello, world!");
}
